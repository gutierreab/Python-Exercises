# Programa que faz a conversão de metros(m), para centímetros(cm) e milímetros(mm).

metros = int(input('Digite um número: '))

centimetros = (metros*100)
milimetros = (metros*1000)

print(f'{metros}m equivalem a {centimetros}cm e a {milimetros}mm.')

