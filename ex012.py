# Programa que calcula a quantidade necessária de tinta para pintar uma parede de área específica.

altura = float(input('Digite a altura da parede: '))
largura = float(input('Digite a largura da parede: '))

area = (altura*largura)
tinta = (area/2)

print(f'A quantidade necessária de tinta será de {tinta}L, para uma área de {area}m2.')

