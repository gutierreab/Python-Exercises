# Programa que realiza a tabuada de um número.

x = int(input('Digite um número: '))

a = (x*1)
b = (x*2)
c = (x*3)
d = (x*4)
e = (x*5)
f = (x*6)
g = (x*7)
h = (x*8)
i = (x*9)
j = (x*10)

print(f'A tabuada de {x} é\n {x}*1={a}\n {x}*2={b}\n {x}*3={c}\n {x}*4={d}\n {x}*5={e}\n {x}*6={f}\n {x}*7={g}')
print(f'{x}*8={h}\n {x}*9={i}\n {x}*10={j}\n')

