# Programa que imprime o dobro, o triplo e a raiz quadrada de um número.

x = int(input('Digite um número: '))

dobro = (x*2)
triplo = (x*3)
raiz_quadrada = (x**(1/2))

print(f'O dobro é {dobro}, o triplo é {triplo} e a raiz quadrada é {raiz_quadrada}')

